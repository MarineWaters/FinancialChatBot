        "💵Биткоин стоит 111 800$\n\n"
        "👷‍♂️В России сотрудники офисов всё чаще идут в штукатуры, маляры, плотники, слесари, электрики и сантехники.\n"
        "💬«Мне бы какой курс ни был, всё хорошо будет», заявил глава ВТБ Костин, отвечая на вопрос о перспективах курса в 95-98 рублей за доллар.\n"
        "🔥Wildberries & Russ купил Рив Гош.\n"
        "🏠Нотариус Корнилова: подарки в виде недвижимости, автомобилей или акций облагаются налогом на доход физлиц.\n"
