        "5. Нельзя добавлять лишний текст, в том числе выводы и рассуждения. Должны быть исключительно эмодзи+новость.\n"
        "6. Нельзя изменять или дополнять смысловое содержание новости, её надо брать как есть. Избегай сильного изменения относительно заголовка.\n"
        "7. Информация берётся исключительно из предоставленной новости. Информация в примере форматирования нужна только для определения формата.\n"
        "8. Отвечать необходимо на русском. На других языках нужно оставлять строго только то, что не было на русском в предоставленной новости.\n"
        "9. Ответ должен быть грамматически и синтаксически корректным для русского языка.\n"
        "10. Отвечай строго согласно необходимому формату предложений эмодзи + сокращённая новость.\n"
